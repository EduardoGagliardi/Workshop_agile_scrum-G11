create extension if not exists pgcrypto with schema extensions;

create schema if not exists app_private;
revoke all on schema app_private from anon, authenticated;

do $$
begin
  if not exists (select 1 from pg_type where typname = 'account_type') then
    create type public.account_type as enum ('Étudiant', 'Formateur');
  end if;

  if not exists (select 1 from pg_type where typname = 'skill_level') then
    create type public.skill_level as enum ('Débutant', 'Intermédiaire', 'Avancé', 'Expert');
  end if;

  if not exists (select 1 from pg_type where typname = 'user_skill_role') then
    create type public.user_skill_role as enum ('Enseignant', 'Apprenant');
  end if;

  if not exists (select 1 from pg_type where typname = 'session_status') then
    create type public.session_status as enum ('Planifiée', 'En cours', 'Terminée', 'Annulée');
  end if;

  if not exists (select 1 from pg_type where typname = 'session_type') then
    create type public.session_type as enum ('Cours rapide', 'Atelier collectif', 'Club thématique');
  end if;

  if not exists (select 1 from pg_type where typname = 'contact_request_status') then
    create type public.contact_request_status as enum ('pending', 'accepted', 'rejected', 'cancelled');
  end if;
end $$;

create table public.users (
  id uuid primary key references auth.users(id) on delete cascade,
  account_type public.account_type not null default 'Étudiant',
  first_name varchar(50) not null,
  last_name varchar(50) not null,
  email varchar(100) not null unique,
  bio text,
  avatar_url text,
  availabilities jsonb not null default '{}'::jsonb,
  experience_points integer not null default 0 check (experience_points >= 0),
  level integer not null default 1 check (level >= 1),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.skills (
  id bigint generated by default as identity primary key,
  name varchar(50) not null unique,
