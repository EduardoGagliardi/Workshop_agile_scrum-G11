create or replace function app_private.validate_session_co_host()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if exists (
    select 1
    from public.sessions
    where id = new.session_id
      and host_id = new.user_id
  ) then
    raise exception 'Session host cannot also be co-host.';
  end if;

  if not exists (
    select 1
    from public.users
    where id = new.user_id
      and account_type = 'Formateur'
  ) then
    raise exception 'Only trainers can join a session as co-host.';
  end if;

  if exists (
    select 1
    from public.session_registrations
    where session_id = new.session_id
      and user_id = new.user_id
  ) then
    raise exception 'A co-host cannot also be registered as learner.';
  end if;

  return new;
end;
$$;

create trigger validate_session_co_host
  before insert or update on public.session_co_hosts
  for each row execute function app_private.validate_session_co_host();

create or replace function app_private.validate_session_registration()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  participant_count integer;
  participant_limit integer;
begin
  if exists (
    select 1
    from public.sessions
    where id = new.session_id
      and host_id = new.user_id
  ) then
    raise exception 'Session host cannot register as learner.';
  end if;

  if exists (
    select 1
    from public.session_co_hosts
    where session_id = new.session_id
      and user_id = new.user_id
  ) then
    raise exception 'Session co-host cannot register as learner.';
  end if;

  select count(sr.user_id), s.max_participants
  into participant_count, participant_limit
  from public.sessions s
  left join public.session_registrations sr on sr.session_id = s.id
  where s.id = new.session_id
  group by s.max_participants;

  if participant_count >= participant_limit then
    raise exception 'Session is full.';
  end if;

  return new;
end;
$$;

create trigger validate_session_registration
  before insert or update on public.session_registrations
  for each row execute function app_private.validate_session_registration();

create or replace function app_private.validate_message_sender()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if not exists (
    select 1
    from public.conversation_participants
    where conversation_id = new.conversation_id
      and user_id = new.sender_user_id
  ) then
    raise exception 'Message sender must belong to the conversation.';
  end if;

  if exists (
    select 1
    from public.conversation_participants other_participant
    join public.user_blocks blocks
      on (blocks.blocker_user_id = new.sender_user_id and blocks.blocked_user_id = other_participant.user_id)
      or (blocks.blocker_user_id = other_participant.user_id and blocks.blocked_user_id = new.sender_user_id)
    where other_participant.conversation_id = new.conversation_id
      and other_participant.user_id <> new.sender_user_id
  ) then
    raise exception 'Cannot send a message to a blocked user.';
  end if;

  return new;
end;
$$;

create trigger validate_message_sender
  before insert on public.messages
  for each row execute function app_private.validate_message_sender();

create or replace function app_private.is_conversation_participant(
  target_conversation_id uuid,
  target_user_id uuid
)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select exists (
    select 1
    from public.conversation_participants
    where conversation_id = target_conversation_id
      and user_id = target_user_id
  );
$$;

