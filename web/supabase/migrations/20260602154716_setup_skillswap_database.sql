create or replace function app_private.skill_swap_level_for_experience(total_experience integer)
returns integer
language plpgsql
immutable
as $$
declare
  remaining_experience integer := greatest(coalesce(total_experience, 0), 0);
  computed_level integer := 1;
  required_experience integer := 500;
begin
  while remaining_experience >= required_experience loop
    remaining_experience := remaining_experience - required_experience;
    computed_level := computed_level + 1;
    required_experience := computed_level * 500;
  end loop;

  return computed_level;
end;
$$;

create or replace function app_private.sync_user_level_from_experience()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  new.experience_points := greatest(coalesce(new.experience_points, 0), 0);
  new.level := app_private.skill_swap_level_for_experience(new.experience_points);

  return new;
end;
$$;

create trigger sync_user_level_from_experience
  before insert or update of experience_points on public.users
  for each row execute function app_private.sync_user_level_from_experience();

create or replace function app_private.prevent_blocked_contact_request()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if exists (
    select 1
    from public.user_blocks
    where (blocker_user_id = new.sender_user_id and blocked_user_id = new.receiver_user_id)
       or (blocker_user_id = new.receiver_user_id and blocked_user_id = new.sender_user_id)
  ) then
    raise exception 'Cannot create a contact request between blocked users.';
  end if;

  return new;
end;
$$;

create trigger prevent_blocked_contact_request
  before insert on public.contact_requests
  for each row execute function app_private.prevent_blocked_contact_request();

create or replace function app_private.create_contacts_after_request_acceptance()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if new.status = 'accepted' and old.status <> 'accepted' then
    insert into public.user_contacts (user_id, contact_user_id)
    values
      (new.sender_user_id, new.receiver_user_id),
      (new.receiver_user_id, new.sender_user_id)
    on conflict (user_id, contact_user_id) do nothing;

    new.responded_at := coalesce(new.responded_at, now());
  end if;

  return new;
end;
$$;

create trigger create_contacts_after_request_acceptance
  before update of status on public.contact_requests
  for each row execute function app_private.create_contacts_after_request_acceptance();

